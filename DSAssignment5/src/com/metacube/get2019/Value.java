package com.metacube.get2019;

/**
 * It is a class with key and value as its attributes.
 * @author Khushi
 *
 */
public class Value {
	
	public String key;
	public String value;
	
	/**
	 * It is constructor to initialize its attributes.
	 * @param key key of dictionary
	 * @param value value of dictionary.
	 */
	public Value(String key, String value) {
		this.key = key;
		this.value = value;
	}
}